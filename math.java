public class math{
	public static void main(String [] args){
		int width=100;
		int height=200;
		double result=0.0;
		result=width*height/2;

		System.out.println("triangle area : "+result);
		System.out.println("square area : "+width*height);		
		int r=10;
		double p=3.14;

		System.out.println("circle area : "+r*r*p);

	

		System.out.println("cylindrical volume : "+r*r*p*height);
	}
}

